pub fn swm1f(z: f32) -> f32 {
    LOOKUP_TABLE
        .into_iter()
        .find(|(threshold, _, _)| z <= *threshold)
        .map(|(_, operation, (num, den))| {
            rational_3_over_3f(
                match operation {
                    Operation::Sqrt => -z / (INV_SQRT_E + sqrtf(z - NEG_INV_E)),
                    Operation::Ln => lnf(-z),
                },
                num,
                den,
            )
        })
        .unwrap_or(f32::NAN)
}
