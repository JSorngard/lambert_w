program xlambw
!
!   Test driver of "sw0", "swm1", "dw0c", and "dwm1c", low- and high-precision
!   procedures computing primary and secondary branch of Lambert W function,
!   W_0(z) or W_-1(z), by piecewise minimax rational function approximation
!
!   Coded by T. Fukushima <Toshio.Fukushima@nao.ac.jp>
!
!   Reference: T. Fukushima (2020) to be submitted
!     "Precise and fast computation of Lambert W-functions by piecewise
!      rational function approximation with variable transformation"
!
implicit real*8 (a-h,o-z)
implicit integer (i-n)
!
parameter (z0=-0.36787944117144232160d0)    ! z0 = -1/e
!
write(*,"(3a25,a10)") "z","zc","W_0","dW"
do j=-1,20
    zc=10.d0**j
    z=zc+z0
    w=dw0c(zc)
    sw=sw0(z)
    dw=(sw-w)/(1.d-16+abs(w))
    write(*,"(1p3e25.15,1pe10.2)") z,zc,w,dw
enddo
do j=40,160,40
    zc=10.d0**j
    z=zc+z0
    w=dw0c(zc)
    sw=sw0(z)
    dw=(sw-w)/(1.d-16+abs(w))
    write(*,"(1p3e25.15,1pe10.2)") z,zc,w,dw
enddo
write (*,*) " "
!
write(*,"(3a25,a10)") "z","zc","W_-1","dW"
do j=-2,-1
    zc=10.d0**j
    z=zc+z0
    w=dwm1c(z,zc)
    sw=swm1(z)
    dw=(sw-w)/(1.d-16+abs(w))
    write(*,"(1p3e25.15,1pe10.2)") z,zc,w,dw
enddo
do j=1,4
    z=-0.1d0**j
    zc=z-z0
    w=dwm1c(z,zc)
    sw=swm1(z)
    dw=(sw-w)/(1.d-16+abs(w))
    write(*,"(1p3e25.15,1pe10.2)") z,zc,w,dw
    z=z*0.3d0
    zc=z-z0
    w=dwm1c(z,zc)
    sw=swm1(z)
    dw=(sw-w)/(1.d-16+abs(w))
    write(*,"(1p3e25.15,1pe10.2)") z,zc,w,dw
enddo
do j=5,145,70
    z=-0.1d0**j
    zc=z-z0
    w=dwm1c(z,zc)
    sw=swm1(z)
    dw=(sw-w)/(1.d-16+abs(w))
    write(*,"(1p3e25.15,1pe10.2)") z,zc,w,dw
enddo
!
end program xlambw
